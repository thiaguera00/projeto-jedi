package ex.projetoJedi2;

public class Exercicio7 {
    public static void main(String[] args) {
        //variavel inicializada
        int num = 1;
        //condição
        switch (num) {
            case 1:
                System.out.println("1");
                break;
            case 2:
                System.out.println("2");
                break;
            case 3:
                System.out.println("3");
                break;
            default:
                System.out.println("0");
        }

    }
}
